"""
This module exposes the input datasets for OceanBench challengers to produce forecast datasets
to evaluate in the benchmark.
"""

import xarray
from oceanbench.core import input_datasets


def glo12_nowcasts() -> xarray.Dataset:
    """
    Open weekly GLO12 nowcasts from 2023 to 2025.

    Returns
    -------
    Dataset
        The Dataset containing GLO12 nowcasts.

    >>> glo12_nowcasts() # doctest: +SKIP
    <xarray.Dataset> Size: ...
    Dimensions:    (time: ..., depth: 50, latitude: 2041, longitude: 4320)
    Coordinates:
      * depth      (depth) float32 200B ...
      * latitude   (latitude) float32 8kB ...
      * longitude  (longitude) float32 17kB ...
      * time       (time) datetime64[ns] ...
    Data variables:
        siconc     (time, latitude, longitude) float32 ...
        sithick    (time, latitude, longitude) float32 ...
        so         (time, depth, latitude, longitude) float32 ...
        thetao     (time, depth, latitude, longitude) float32 ...
        uo         (time, depth, latitude, longitude) float32 ...
        usi        (time, latitude, longitude) float32 ...
        vo         (time, depth, latitude, longitude) float32 ...
        vsi        (time, latitude, longitude) float32 ...
        zos        (time, latitude, longitude) float32 ...
    """

    return input_datasets.glo12_nowcasts()


